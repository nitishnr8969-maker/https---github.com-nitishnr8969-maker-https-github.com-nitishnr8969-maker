public class SimulateDivision { 
   public static void main(String[] args) { 
       int dividend = 20, divisor = 3; 
       int quotient = 0; 
       for(; dividend >= divisor; ) { 
           dividend -= divisor; 
           quotient++; 
       } 
       System.out.println("Quotient: " + quotient); 
   } 
}

