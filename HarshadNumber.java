public class HarshadNumber { 
   public static void main(String[] args) { 
     int n = 18; 
       int temp = n; 
       int sum = 0; 
       for(; temp != 0; temp /= 10) 
           sum += temp % 10; 
       if(n % sum == 0) 
           System.out.println("Harshad Number"); 
       else 
           System.out.println("Not Harshad Number"); 
   } 
} 

