import java.util.Scanner;
public class TemperatureCheck{
    public static void main(String[] args){
        Scanner sc=new Scanner(System.in);
        int temp=sc.nextInt();
        if(temp>35){
            System.out.print("Hot");
        }
        else{
            System.out.print("Cold");
        }
    }
}

