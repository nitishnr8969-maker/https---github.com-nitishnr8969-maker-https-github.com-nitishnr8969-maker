import java.util.Scanner;
public class PassFail{
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        int marks=sc.nextInt();
        if(marks>=40){
            System.out.print("Pass");
        }
        else{
            System.out.print("Fail");
        }
        }
    }


