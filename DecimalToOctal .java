public class DecimalToOctal { 
   public static void main(String[] args) { 
       int n = 100; 
       int octal = 0, place = 1; 
       for(; n != 0; n /= 8) { 
           octal += (n % 8) * place; 
           place *= 10; 
       } 
       System.out.println("Octal: " + octal); 
   } 
} 

